struct CompiledInstruction {
  static constexpr std::uint8_t kFlagIsGlobal = 1u << 0;
  static constexpr std::uint8_t kFlagIsLoad = 1u << 1;
  static constexpr std::uint8_t kFlagIsSignedLoad = 1u << 2;
  static constexpr std::uint8_t kFlagHasReturn = 1u << 3;

  CompiledOpcode opcode = CompiledOpcode::kSEndpgm;
  std::array<InstructionOperand, DecodedInstruction::kMaxOperands> operands{};
  std::uint8_t operand_count = 0;
  std::uint8_t flags = 0;
  std::uint8_t register_dword_count = 0;
  std::uint8_t element_size_bytes = 0;
  std::uint8_t memory_dword_count = 0;
  std::uint8_t data_dword_count = 0;

  bool IsGlobal() const { return (flags & kFlagIsGlobal) != 0; }
  bool IsLoad() const { return (flags & kFlagIsLoad) != 0; }
  bool IsSignedLoad() const { return (flags & kFlagIsSignedLoad) != 0; }
  bool HasReturn() const { return (flags & kFlagHasReturn) != 0; }
};

